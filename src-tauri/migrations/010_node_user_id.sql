-- Migration 010: owner user of a node (message).
-- Заготовка «на будущее» для облачной синхронизации сообщений нескольких
-- зарегистрированных пользователей. Локально одна личность = одна база, поэтому
-- сейчас поле НЕ используется и остаётся NULL у всех существующих и новых узлов.
-- Когда появится аккаунт/синхронизация — сюда пишется идентификатор владельца.

ALTER TABLE nodes ADD COLUMN user_id TEXT; -- owner user id (NULL = локальная одиночная личность)

CREATE INDEX IF NOT EXISTS idx_nodes_user_id ON nodes(user_id);
