// Контракт подсистемы рендеринга текста ответов (шаг 1: каркас + база).
//
// Идея «слоёв» (как у виджет-плагинов, но это ОТДЕЛЬНАЯ поверхность — рендеринг
// сообщения, а не панель контролов): базовый рендерер работает всегда; спец-
// плагины ДОПОЛНЯЮТ его, не трогая базовый UI. Сменился плагин/модель — появились
// новые способы выделения/форматирования внутри пузыря, ядро при этом не меняется.
//
// Поверхность узкая под реальную нужду (ср. D-074): две точки расширения —
//   1) ContentParser  — как строка ответа режется на сегменты (по умолчанию —
//      базовый markdown-парсер; LLM-специфичный плагин может переопределить, если
//      у конкретной модели нестандартная разметка — отбор по supportedModels);
//   2) SegmentRenderer — как рисуется ОДИН сегмент (базовые рисуют текст и код;
//      спец-плагин может перехватить, напр., только code-сегменты языка X).
//
// Шаг 1 реализует только базу; спец-плагины (подсветка синтаксиса и т.п.) —
// позже, но контракт уже оставляет для них место.

import type { ReactNode } from 'react';
import type { ModelFacts } from '../../widgets/host/types';

// Результат разбора ответа. Намеренно плоский и минимальный: текст и блок кода.
// Инлайн-код базовый текстовый рендерер разбирает ВНУТРИ text-сегмента — отдельным
// видом сегмента его не делаем (мельчить контракт без нужды не хотим).
export type Segment =
  | { kind: 'text'; content: string }
  | { kind: 'code'; lang: string | null; content: string };

// Подмножество WidgetFacts, нужное рендерерам как «контекст». На шаге 1 базовый
// рендерер их НЕ использует, но спец-плагины будут (язык из тегов диалога,
// применимость к активной модели/провайдеру). Структурно совместимо с WidgetFacts,
// поэтому в точке вызова можно передать факты контроллера как есть.
export interface RenderFacts {
  activeLlmProviderId: string | null;
  model: ModelFacts;
  dialogTags: string[];
}

// Манифест рендерера/парсера. supportedModels — применимость к активной модели
// (та же семантика, что у виджетов: отсутствует/'*' => любая; иначе точное
// совпадение либо префикс с хвостовой '*'). order — приоритет выбора (меньше =
// раньше); базовые сущности держат большой order, чтобы быть фолбэком.
export interface RendererManifest {
  id: string;
  title: string;
  supportedModels?: string[] | '*';
  order?: number;
  author?: string;
  version?: string;
  sourceUrl?: string;
}

// Парсер строки ответа в сегменты. Один активный на сообщение (выбор по
// supportedModels против facts.model.id; базовый — фолбэк).
export interface ContentParser {
  manifest: RendererManifest;
  parse(content: string, facts: RenderFacts): Segment[];
}

// Рендерер одного сегмента. matches решает, берётся ли он за данный сегмент
// (с учётом facts: язык/тег/модель). Несколько могут быть активны — выбирается
// первый подходящий по порядку; базовые матчат по виду сегмента и стоят последними.
export interface SegmentRenderer {
  manifest: RendererManifest;
  matches(segment: Segment, facts: RenderFacts): boolean;
  render(segment: Segment, facts: RenderFacts): ReactNode;
}
